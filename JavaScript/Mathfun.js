// Using var
var a = 10;
var b = 20;
var sum = a + b;
console.log('Sum using var:', sum);

// Using let
let x = 15;
let y = 25;
let product = x * y;
console.log('Product using let:', product);

// Using const
const p = 30;
const q = 5;
const division = p / q;
console.log('Division using const:', division);
